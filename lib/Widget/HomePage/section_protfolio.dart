import 'package:scrollable_list_tabview/scrollable_list_tabview.dart';

// ignore: camel_case_types
class Section_Protfolio extends StatefulWidget {
  const Section_Protfolio({
    Key? key,
  }) : super(key: key);

  @override
  _Section_ProtfolioState createState() => _Section_ProtfolioState();
}

// ignore: camel_case_types
class _Section_ProtfolioState extends State<Section_Protfolio> {
  late Future<Autogenerated?> apiCall;

  List getCategory(
    List data,
    String name,
  ) {
    List subgategoray = [];
    for (var a in data)
      if (data.contains(a.name == 1)) subgategoray.add(a.name == 1);

    return subgategoray;
  }

  @override
  void initState() {
    super.initState();
    apiCall = getdatapublic();
  }


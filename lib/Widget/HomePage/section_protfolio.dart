                                  height: 10,
                                ),
                                Text(
                                  snapshot.data!.data.portfolio[index].name
                                      .toString(),
                                  style: TextStyle(
                                      color: Color(0xff2c4755),
                                      fontWeight: FontWeight.bold),
                                ),
                                SizedBox(
                                  height: 10,
                                ),
                                Text(snapshot.data!.data.portfolio[index].desc),
                                Divider(
                                  thickness: 1,
                                  height: 20,
                                )
                              ],
                            );
                          }),
                    ),
                    ScrollableListTab(
                      tab: ListTab(
                          label: Text('Marketing'), showIconOnList: false),
                      body: ListView.builder(
                          physics: NeverScrollableScrollPhysics(),
                          primary: false,
                          shrinkWrap: true,
                          itemCount: getCategory(
                                  snapshot.data!.data.portfolio, 'pcategory_id')

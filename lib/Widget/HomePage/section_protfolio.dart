                                  decoration: BoxDecoration(
                                      color: Colors.grey[200],
                                      borderRadius:
                                          BorderRadius.all(Radius.circular(5)),
                                      image: DecorationImage(
                                          fit: BoxFit.fitWidth,
                                          image: getimage(snapshot.data!.data
                                              .portfolio[index].cover))),
                                ),
                                SizedBox(
                                  height: 10,
